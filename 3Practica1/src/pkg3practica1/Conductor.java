package pkg3practica1;

public class Conductor {

    public String nombre;
    public int edad;
    //constructor

    public Conductor() {

    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

}
